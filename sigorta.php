    function hesapla() {
  // Form verilerini al
  var yuzolcumu = document.getElementById("yuzolcumu").value;
  var birimsigortabedeli = document.getElementById("birimsigortabedeli").value;
  var binayasfaktoru = document.getElementById("binayasfaktoru").value;
  var binaustsinif = document.getElementById("binaustsinif").value;
  var binainsayili = document.getElementById("binainsayili").value;
  var katsayisi = document.getElementById("katsayisi").value;
  var yuzolcumu = document.getElementById("yuzolcumu").value;

  // Hesaplama işlemi
  var dasksigortabedeli = yuzolcumu * birimsigortabedeli * binayasfaktoru;

  // Bina yapı tarzı faktörü hesapla
  var binayapitarzifaktoru = 1;
  if (binaustsinif === "Betonarme") {
    binayapitarzifaktoru = 1.5;
  } else if (binaustsinif === "Çelik") {
    binayapitarzifaktoru = 1.3;
  } else if (binaustsinif === "Ahşap") {
    binayapitarzifaktoru = 1.2;
  }

  // Kat sayısı faktörü hesapla
  var katsayisifaktoru = 1 + (0.1 * katsayisi);

  // Toplam sigorta bedelini hesapla
  var toplamsigortabedeli = dasksigortabedeli * binayapitarzifaktoru * katsayisifaktoru;

  // Sonucu ekrana yazdır
  var sonucDiv = document.getElementById("sonuc");
  sonucDiv.innerHTML = "<p>Toplam Sigorta Bedeli: " + toplamsigortabedeli.toFixed(2) + " TL</p>";
}

</script>
